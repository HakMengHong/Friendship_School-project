const { PrismaClient } = require('@prisma/client')
const prisma = new PrismaClient()

async function deleteAttendance() {
  console.log('\nüóëÔ∏è  Deleting attendance records generated by System...\n')
  
  try {
    // Count before deletion
    const beforeCount = await prisma.attendance.count()
    console.log(`üìä Current attendance records: ${beforeCount}`)
    
    // Delete attendance records created by System
    const result = await prisma.attendance.deleteMany({
      where: {
        recordedBy: 'System'
      }
    })
    
    console.log(`\n‚úÖ Deleted ${result.count} attendance record(s) created by System`)
    
    // Count after deletion
    const afterCount = await prisma.attendance.count()
    console.log(`üìä Remaining attendance records: ${afterCount}`)
    
  } catch (error) {
    console.error('\n‚ùå Error deleting attendance:', error)
    throw error
  } finally {
    await prisma.$disconnect()
  }
}

deleteAttendance()
  .then(() => {
    console.log('\n‚úÖ Deletion completed successfully!')
    process.exit(0)
  })
  .catch((error) => {
    console.error('\n‚ùå Deletion failed:', error)
    process.exit(1)
  })

